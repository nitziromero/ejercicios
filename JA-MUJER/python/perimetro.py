# Pedir al usuario que ingrese la longitud de un lado del cuadrado
lado = float(input("Ingresa la longitud de un lado del cuadrado: "))

# Calcular el perímetro del cuadrado (perímetro = 4 * lado)
perimetro = 4 * lado

# Mostrar el resultado al usuario
print(f"El perímetro del cuadrado con lado {lado} es {perimetro}")
