// Paso 1: Definir la temperatura en grados Celsius.
var celsius = 25; // Puedes cambiar este valor por la temperatura en grados Celsius que desees convertir.

// Paso 2: Aplicar la fórmula de conversión a Fahrenheit.
var fahrenheit = (celsius * 9/5) + 32;

// Paso 3: Imprimir el resultado en la consola.
console.log(celsius + " grados Celsius equivalen a " + fahrenheit + " grados Fahrenheit.");
